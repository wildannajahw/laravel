                <h4>While all people are created equal, every individual body is unique. Yet, in today’s increasingly personalized world, the experience of buying clothing is still extremely generic. The traditional sizing system is broken. It creates unnecessary labels: Petite, Curvy, Tall, Plus. It implies we’re always too something: too short, too tall, too round, too thin, too broad, too straight... It suggests that if we don’t fit, we are to blame.</h4>
                <h4>At HolyShirt, we love shapes not sizes. So, we’ve been rethinking how clothing is made.</h4>
                <h4>Welcome to a size-free world. Here, we create clothing patterns using real people in dozens of diverse shapes and sizes. We then use our revolutionary HolyShirt measurement system along with proprietary algorithms to capture your precise measurements and to make you custom-fit clothing that feels tailor-made.</h4>
                <h4>“HolyShirt was created to be adaptable to each and every person. You don’t have to adapt to HolyShirt. HolyShirt adapts to you. People are unique, but they also want to be treated and accepted as equal. This concept is reflected in the HolyShirt logo. The circle, square and triangles are all different colors and shapes, yet they have the same surface area. They are all unique but still equal.” - Yusaku Maezawa, HolyShirt founder</h4>
